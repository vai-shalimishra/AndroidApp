      home: CanteenHomePage(),

    );
  }
}

class CanteenHomePage extends StatefulWidget {
  @override
  _CanteenHomePageState createState() => _CanteenHomePageState();
}

class _CanteenHomePageState extends State<CanteenHomePage> {
  List<String> _menuItems = [    'Samosa','Burger',    'Pizza',    'Sandwich',    'French Fries',    'Coke',    'Pepsi',    'Iced Tea',  ];

  List<String> _selectedItems = [];
  int _quantity = 1;
  void _addToCart(String item) {

    setState(() {
      _quantity++;
      _selectedItems.add(item);
    });
  }

  void _removeFromCart(String item) {
    setState(() {
      _quantity = _quantity > 1 ? _quantity - 1 : 1;
      _selectedItems.remove(item);
    });
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text('Canteen'),
        actions: [
          IconButton(
            icon: Icon(Icons.shopping_cart),
            onPressed: () {
              Navigator.push(
                context,
                MaterialPageRoute(
                  builder: (context) => CartPage(selectedItems: _selectedItems),
                ),
              );
            },
          ),
        ],
      ),
      
      body: ListView.builder(
        itemCount: _menuItems.length,
        itemBuilder: (context, index) {
          final item = _menuItems[index];
          return ListTile(
            title: Text(item),
            trailing: _selectedItems.contains(item)
                ? IconButton(
              icon: Icon(Icons.remove),
              onPressed: () => _removeFromCart(item),
            )
                : IconButton(
              icon: Icon(Icons.add),
              onPressed: () => _addToCart(item),
            ),
          );
        },
      ),
    );
  }
}

class CartPage extends StatelessWidget {
  final List<String> selectedItems;

  CartPage({required this.selectedItems});

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text('Cart'),
      ),
      body: ListView.builder(
        itemCount: selectedItems.length,
        itemBuilder: (context, index) {
          final item = selectedItems[index];
          return ListTile(
            title: Text(item),
          );
        },
      ),
    );
  }
}

